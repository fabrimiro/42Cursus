#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	n;

	if (*needle == '\0')
		return ((char *)haystack);
	n = ft_strlen(needle);
	while (*haystack && n <= len--)
	{
		if (!ft_strncmp(haystack, needle, n))
			return ((char *)haystack);
		haystack++;
	}
	return (NULL);
}
