#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t			x;
	unsigned char	*y;

	x = 0;
	y = str;
	while (n > 0)
	{
		y[x] = c;
		x++;
		n--;
	}
	return (str);
}
